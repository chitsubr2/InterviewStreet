				int numbers[] = new int[numberStrs.length];
				for (int j = 0; j < numberStrs.length; j++) {
					numbers[j] = Integer.parseInt(numberStrs[j]);
				}
				if (doesAliceToPlayWin(numbers, maxN)) {
					System.out.println("Alice");
				}	
